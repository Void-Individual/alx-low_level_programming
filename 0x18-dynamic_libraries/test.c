#include "main.h"
/**
 * add - function to add num
 * @a: value 1
 * @b: value 2
 * Return: sum of values
*/

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function to subtract num
 * @a: value 1
 * @b: value 2
 * Return: diff of values
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function to multiply num
 * @a: value 1
 * @b: value 2
 * Return: multiple of values
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function to divide num
 * @a: value 1
 * @b: value 2
 * Return: divisor of values
*/

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function to div into remainder for num
 * @a: value 1
 * @b: value 2
 * Return: remainder of values
*/

int mod(int a, int b)
{
	return (a % b);
}
